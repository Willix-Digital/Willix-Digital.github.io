            document.addEventListener('DOMContentLoaded', function() {
                // Sélection des éléments
                let afficheGrid = document.querySelector("#row-affiche");
                let logoGrid = document.querySelector("#row-logo");
                let mockupeGrid = document.querySelector("#row-mokup");
                let webciteGrid = document.querySelector("#row-website");
                let ZoneDAaffichage = document.querySelector("#zone-Affichage");

                let btnAffiche = document.querySelector("#btn-affiche");
                let btnLogo = document.querySelector("#btn-logo");
                let btnMockup = document.querySelector("#btn-mockup");
                let btnWebcite = document.querySelector("#btn-website");

                // Fonction pour afficher les logos par défaut
                function affichageLogo() {
                    ZoneDAaffichage.innerHTML = '';
                    ZoneDAaffichage.appendChild(logoGrid);
                    logoGrid.style.display = "flex";
                    btnLogo.style.backgroundColor = "#d4adfc";
                    btnAffiche.style.backgroundColor = "#29294a";
                    btnMockup.style.backgroundColor = "#29294a";
                    btnWebcite.style.backgroundColor = "#29294a";
                }

                // Afficher les logos au chargement de la page
                affichageLogo();

                // Événements de clic
                btnAffiche.addEventListener('click', function() {
                    ZoneDAaffichage.innerHTML = '';
                    ZoneDAaffichage.appendChild(afficheGrid);
                    afficheGrid.style.display = "flex";

                    btnAffiche.style.backgroundColor = "#d4adfc";
                    btnLogo.style.backgroundColor = "#29294a";
                    btnMockup.style.backgroundColor = "#29294a";
                    btnWebcite.style.backgroundColor = "#29294a";
                });

                btnMockup.addEventListener('click', function() {
                    ZoneDAaffichage.innerHTML = '';
                    ZoneDAaffichage.appendChild(mockupeGrid);
                    mockupeGrid.style.display = "flex";

                    btnMockup.style.backgroundColor = "#d4adfc";
                    btnLogo.style.backgroundColor = "#29294a";
                    btnAffiche.style.backgroundColor = "#29294a";
                    btnWebcite.style.backgroundColor = "#29294a";
                });

                btnLogo.addEventListener('click', affichageLogo);
            });

